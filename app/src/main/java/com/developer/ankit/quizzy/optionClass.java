package com.developer.ankit.quizzy;


public class optionClass {
    private int A;
    private int B;
    private int C;
    private int D;
    public optionClass(int A, int B, int C, int D){
        this.A = A;
        this.B = B;
        this.C = C;
        this.D = D;
    }

    public int getA() {
        return A;
    }

    public int getB() {
        return B;
    }

    public int getC() {
        return C;
    }

    public int getD() {
        return D;
    }
}
